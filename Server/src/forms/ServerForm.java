    private void startServer()
    {
        if (thread == null || !thread.isAlive())
        {
            try
            {
                serverLogic = new ServerThread();
                thread = new Thread(serverLogic);

                thread.start();

                btnStartServer.setEnabled(false);
                btnStopServer.setEnabled(true);

                lblStatus.setText("Status: RUNNING");
            }
            catch (IOException ex)
            {
                JOptionPane.showMessageDialog(rootPane, "Startup Error\n" + ex.getMessage(), "ERROR", ERROR);
                ex.printStackTrace();
            }
        }
    }

