    private void stopServer()
    {
        if (thread != null && thread.isAlive())
        {
            serverLogic.stopServer();

            btnStartServer.setEnabled(true);
            btnStopServer.setEnabled(false);

            lblStatus.setText("Status: STOPPED");
        }
    }

