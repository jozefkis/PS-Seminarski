    private void prepareDialog()
    {
        populateComboTravar();
        populateComboKupac();
        loadTableData();
        checkTravar.setSelected(false);
        checkKupac.setSelected(false);
    }

    private void populateComboTravar()
    {
        try
        {
            comboTravar.removeAllItems();
            List<Travar> all = ClientController.getInstance().getAllTravar();

            for (Travar t : all)
            {
                comboTravar.addItem(t);
            }

            comboTravar.setEnabled(false);
            AutoCompleteCBDecoratorUtils.decorate(comboTravar, all);
            comboTravar.setSelectedIndex(-1);

        }
        catch (Exception ex)
        {
            System.getLogger(SearchRacunDialog.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }

    private void loadTableData()
    {
        try
        {
            List<Racun> all = ClientController.getInstance().getAllRacun(new Racun());
            tblRacuni.setModel(new TableModelRacun(all));
            
            tblRacuni.getColumnModel().getColumn(2).setPreferredWidth(150);
        }
        catch (Exception ex)
        {
            System.getLogger(SearchRacunDialog.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }

    private void populateComboKupac()
    {
        try
        {
            comboKupac.removeAllItems();
            List<Kupac> all = ClientController.getInstance().getAllKupac();

            for (Kupac k : all)
            {
                comboKupac.addItem(k);
            }

            comboKupac.setEnabled(false);
            AutoCompleteCBDecoratorUtils.decorate(comboKupac, all);
            comboKupac.setSelectedIndex(-1);

        }
        catch (Exception ex)
        {
            System.getLogger(SearchRacunDialog.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }
