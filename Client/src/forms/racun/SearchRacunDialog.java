    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSearchActionPerformed
    {//GEN-HEADEREND:event_btnSearchActionPerformed
        // TODO add your handling code here:
        Racun r = new Racun();
        if (comboTravar.getSelectedItem() instanceof Travar)
        {
            r.setTravar((Travar) comboTravar.getSelectedItem());
        }
        else
        {
            r.setTravar(null);
        }

        if (comboKupac.getSelectedItem() instanceof Kupac)
        {
            r.setKupac((Kupac) comboKupac.getSelectedItem());
        }
        else
        {
            r.setKupac(null);
        }

        try
        {
            List<Racun> filtered = ClientController.getInstance().getAllRacun(r);
            ((TableModelRacun) tblRacuni.getModel()).setRacuni(filtered);
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnSearchActionPerformed

