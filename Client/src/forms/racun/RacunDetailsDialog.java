    private void btnAddStavkaActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAddStavkaActionPerformed
    {//GEN-HEADEREND:event_btnAddStavkaActionPerformed
        if (!(comboCaj.getSelectedItem() instanceof Caj))
        {
            JOptionPane.showMessageDialog(this, "Morate izabrati čaj.", "Poruka", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Caj c = (Caj) comboCaj.getSelectedItem();
        int kolicina = (int) spnrKolicina.getValue();

        StavkaRacuna sr = new StavkaRacuna();

        sr.setCaj(c);
        sr.setKolicina(kolicina);
        sr.setCena(c.getCena());
        sr.setIznos(kolicina * c.getCena());
        sr.setRacun(ogRacun);
        sr.setStatus("DB_INSERT");

        boolean result = model.addStavka(sr);

        if (!result)
        {
            JOptionPane.showMessageDialog(this, "Već ste dodali taj čaj", "", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        ogRacun.setDatum(LocalDateTime.now());
        DateTimeFormatter customFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        lblDate.setText(String.valueOf(ogRacun.getDatum().toLocalDate().format(customFormatter))
                + ", " + ogRacun.getDatum().toLocalTime().truncatedTo(ChronoUnit.SECONDS));
    }//GEN-LAST:event_btnAddStavkaActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveActionPerformed
    {//GEN-HEADEREND:event_btnSaveActionPerformed
        try
        {
            ogRacun.setDatum(LocalDateTime.now());
            ClientController.getInstance().updateRacun(ogRacun);
            JOptionPane.showMessageDialog(this, "Sistem je uspešno zapamtio račun.", "Uspešno ažuriranje", JOptionPane.INFORMATION_MESSAGE);
            ((SearchRacunDialog) this.getParent()).refreshTable();
            this.dispose();
        }
        catch (Exception ex)
        {
            System.out.println("GREKSA: " + ex.getMessage() + "\n");
            JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti račun.", "Greška", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();

        }
    }//GEN-LAST:event_btnSaveActionPerformed

