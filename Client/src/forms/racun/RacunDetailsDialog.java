    private void prepareView()
    {
        populateCajCombo();

        DateTimeFormatter customFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        
        lblID.setText(String.valueOf(ogRacun.getIdRacun()));
        lblDate.setText(String.valueOf(ogRacun.getDatum().toLocalDate().format(customFormatter)) + ", " + ogRacun.getDatum().toLocalTime());
        lblTravar.setText(ogRacun.getTravar().toString());
        lblKupac.setText(ogRacun.getKupac().toString());
        lblTotal.setText(String.valueOf(ogRacun.getUkupanIznos()));

        if (ogRacun.getDatum().plusDays(3L).isBefore(LocalDateTime.now()))
        {
            btnAddStavka.setEnabled(false);
            btnSave.setEnabled(false);
            tblStavke.setEnabled(false);
            btnDeleteStavka.setEnabled(false);
        }

    }

    private void populateCajCombo()
    {
        try
        {
            List<Caj> all = ClientController.getInstance().getAllCaj();
            comboCaj.removeAllItems();

            for (Caj caj : all)
            {
                comboCaj.addItem(caj);
            }

            AutoCompleteCBDecoratorUtils.decorate(comboCaj, all);
            comboCaj.setSelectedIndex(-1);
        }
        catch (Exception e)
        {
            System.out.println("comboCaj error");
            System.out.println(e.getMessage());
        }
    }

    private void updateUkupno()
    {
        double ukupno = model.getRacun().getUkupanIznos();
        lblTotal.setText(String.format("%.2f", ukupno));
        System.out.println(ogRacun.getUkupanIznos());
    }
