    private void btnDeleteStavkaActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDeleteStavkaActionPerformed
    {//GEN-HEADEREND:event_btnDeleteStavkaActionPerformed
        int viewRow = tblStavke.getSelectedRow();
        if (viewRow == -1)
        {
            JOptionPane.showMessageDialog(this, "Izaberite stavku za brisanje.");
            return;
        }

        int modelRow = tblStavke.convertRowIndexToModel(viewRow);

        StavkaRacuna toDelete = model.getRacun().getStavkeRacuna().get(modelRow);

        if ("DB_INSERT".equals(toDelete.getStatus()))
            model.removeStavka(toDelete);
        else
            model.deleteStavka(toDelete);
        
        ogRacun.setDatum(LocalDateTime.now());
        DateTimeFormatter customFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        lblDate.setText(String.valueOf(ogRacun.getDatum().toLocalDate().format(customFormatter))
                + ", " + ogRacun.getDatum().toLocalTime().truncatedTo(ChronoUnit.SECONDS));
    }//GEN-LAST:event_btnDeleteStavkaActionPerformed

